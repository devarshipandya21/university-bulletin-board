    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
               
                  String ID_input=jTextField1.getText();
                  String pass_input=new String(jPasswordField1.getPassword());
                    try
                    {
                        Class.forName("java.sql.DriverManager");
                        Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/charusat","root","admin");
                        Statement st=(Statement)con.createStatement();
                        String query="select * from login";
                        ResultSet rs=st.executeQuery(query);
                        int flag=0;
                        while(rs.next())
                        {
                            String ID=rs.getString("id_no");
                            String pass=rs.getString("password");
                            String branch=rs.getString("branch");
                            if(ID.equals("admin") && pass.equals(pass_input))
                            {
                                JOptionPane.showMessageDialog(null, "You have successfully logged in!");
                                Admin_Panel_Main A1=new Admin_Panel_Main();
                                A1.setVisible(true);
                                this.dispose();
                                flag=0;
                                break;
                            }
                            else
                            {
                                if(pass.equals(pass_input) && ID.equals(ID_input))
                                {
                                    Logged_In=ID;
                                    Logged_In_Branch=branch;
                                    JOptionPane.showMessageDialog(null, "You have successfully logged in!");
                                    Student_Panel sp1=new Student_Panel();
                                    sp1.setVisible(true);
                                    this.dispose();
                                    flag=0;
                                    break;
                                } 
                                else 
                                {
                                    flag=1;
                                }
                            }
                        }
                        if(flag==1)
                            JOptionPane.showMessageDialog(null,"Incorrect ID Number or Password!");

                    }catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(this,e.getMessage());
                    }
            
    }//GEN-LAST:event_jButton1ActionPerformed
