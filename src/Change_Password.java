    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ID_input;
        ID_input = Login.LoggedIn();
        
        try
                    {
                        Class.forName("java.sql.DriverManager");
                        Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/charusat","root","admin");
                        Statement st=(Statement)con.createStatement();
                        String query="select * from login";
                        ResultSet rs=st.executeQuery(query);
                        int flag=0;
                        while(rs.next())
                        {
                            String ID=rs.getString("id_no");
                            String pass=rs.getString("password");
                            if((ID.equals(ID_input)) && (pass.equals(jPasswordField1.getText())) && (jPasswordField2.getText().equals(jPasswordField3.getText())))
                            {
                                String query1="update login set password='"+jPasswordField2.getText()+"' where id_no='"+ID+"'";
                                st.executeUpdate(query1);
                                JOptionPane.showMessageDialog(null, "New password set successfully.");
                                Login L1=new Login();
                                L1.setVisible(true);
                                this.dispose();
                                flag=0;
                                break;
                            }
                            else
                                flag=1;
                        }
                        if(flag==1)
                            JOptionPane.showMessageDialog(null, "Passwords do not match!");

                    }

                    catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(this,e.getMessage() );
                    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

