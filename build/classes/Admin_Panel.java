    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String branch=jComboBox1.getSelectedItem().toString();
        String sem=jComboBox3.getSelectedItem().toString();
        String division=jComboBox2.getSelectedItem().toString();
        String info=jTextArea1.getText();
        DateFormat df=new SimpleDateFormat("dd/MM/yy HH:mm:ss");
        java.util.Date obj=new java.util.Date();
        String dt=df.format(obj);
        try
            {
                Class.forName("java.sql.DriverManager");
                Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/charusat","root","admin");
                Statement st=(Statement)con.createStatement();
                String query="insert into information values('"+branch+"','"+sem+"','"+division+"','"+info+"','"+dt+"')";
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(null,"Successfully posted update in "+jComboBox1.getSelectedItem().toString()+"-"+jComboBox2.getSelectedItem().toString()+" Semester-"+jComboBox3.getSelectedItem().toString());
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage() );
            }
        if(jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Information field has been left empty.");
        } 
        
    }//GEN-LAST:event_jButton1ActionPerformed

