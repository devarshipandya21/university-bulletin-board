    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id=Login.LoggedIn();
        String ask_query=jTextArea1.getText();
        DateFormat df=new SimpleDateFormat("dd/MM/yy HH:mm:ss");
        java.util.Date obj=new java.util.Date();
        String dt=df.format(obj);
        try
        {
            Class.forName("java.sql.DriverManager");
            Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/charusat","root","admin");
            Statement st=(Statement)con.createStatement();
            String query="insert into queries values('"+id+"','"+ask_query+"','"+dt+"','')";
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(null,"Your query has been sent. We will revert back soon.");
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
        if(jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Query field has been left empty.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

